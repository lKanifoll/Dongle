/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */

  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */
	if ((READ_REG(huart1.Instance->ISR) & USART_ISR_IDLE) != RESET)
	{
		SET_BIT(huart1.Instance->ICR, USART_ICR_IDLECF);
	
		__HAL_DMA_DISABLE(&hdma_usart1_rx);
		
		modbus_raw_byte_count = BUFFER_SIZE - hdma_usart1_rx.Instance->CNDTR; 															// calc received size
		
		if(modbus_raw_byte_count > 0) // > 0??? 
		{ 
			//memcpy((void*)modbus_union.modbus_master_cmd, (const void*)rx_485_buff, modbus_master_byte_cnt); 							// Copy from dma buffer to master buffer
			
			//memset((void*)rx_485_buff, 0, BUFFER_SIZE);   	// realy need???														// clear buff
			hdma_usart1_rx.Instance->CNDTR = BUFFER_SIZE;																				// Reset DMA counter
			modbus_slave_address = 228;
			if (modbus_raw.modbus_frame.address == modbus_slave_address)
			{
				if ((modbus_rtu_calc_crc(modbus_raw.modbus_master_frame, modbus_raw_byte_count)) == 0)									// check CRC
				{															
					modbus_rx_complete = TRUE;																						// 
				}
				else
				{
					//return CRC ERROR
				}
			}
		}
		//HAL_UART_RxCpltCallback(&huart1);
		__HAL_DMA_ENABLE(&hdma_usart1_rx);
	}
  /* USER CODE END USART1_IRQn 1 */
}

